package eu.easyrpa.openframework.word.constants;

//TODO update javadoc
/**
 * Helps to identify the horizontal alignment of text in a cell.
 */
public enum TextAlignment {

    /**
     * The horizontal alignment is not specified.
     */
    UNSPECIFIED,

    /**
     * The text aligned to the left of the cell.
     */
    LEFT,

    /**
     * The text aligned to the center of the cell.
     */
    CENTER,

    /**
     * The text aligned to the right of the cell.
     */
    RIGHT,

    /**
     *
     */
    JUSTIFY;
}
