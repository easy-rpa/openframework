package eu.easyrpa.openframework.google.sheets.constants;

/**
 * Helps to identify the vertical alignment of text in a cell.
 */
public enum VerticalAlignment {

    /**
     * The vertical alignment is not specified.
     */
    UNSPECIFIED,

    /**
     * The text aligned to the top of the cell.
     */
    TOP,

    /**
     * The text aligned to the middle of the cell.
     */
    MIDDLE,

    /**
     * The text aligned to the bottom of the cell.
     */
    BOTTOM;
}
